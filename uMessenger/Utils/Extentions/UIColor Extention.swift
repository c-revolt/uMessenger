import UIKit

extension UIColor {
    
    static func mainBlack() -> UIColor {
        return #colorLiteral(red: 0.1298427582, green: 0.1298427582, blue: 0.1298427582, alpha: 1)
    }
    
    static func mainBlue() -> UIColor {
        return #colorLiteral(red: 0.234964788, green: 0.3151938319, blue: 0.9857624173, alpha: 1)
    }
    
    static func mainPink() -> UIColor {
        return #colorLiteral(red: 0.9851433635, green: 0.3535805643, blue: 0.6425073147, alpha: 1)
    }
}
